	// ПАМЯТКА:

	// PROGMEM — хранит таблицу символов во Flash, а не в SRAM.
	
	// каждый символ хранится как 5 байтов.
	// каждый байт — это один вертикальный столбец символа.
	//
	// символ имеет размер 5x7:
	// 5 столбцов по ширине
	// 7 строк по высоте
	//
	// хотя один байт содержит 8 бит, мы используем только 7 младших бит:
	//
	// bit 0 -> строка 0, верхний пиксель столбца
	// bit 1 -> строка 1
	// bit 2 -> строка 2
	// bit 3 -> строка 3
	// bit 4 -> строка 4
	// bit 5 -> строка 5
	// bit 6 -> строка 6, нижний пиксель столбца
	// bit 7 -> не используется
	//
	// то есть при отрисовке мы идём от младшего бита к старшему,
	// но проверяем только bit0..bit6.
	// старший bit7 просто игнорируется.
	//
	// пример:
	// 0x7E = 0b01111110
	//
	// bit0 = 0 -> строка 0: .
	// bit1 = 1 -> строка 1: #
	// bit2 = 1 -> строка 2: #
	// bit3 = 1 -> строка 3: #
	// bit4 = 1 -> строка 4: #
	// bit5 = 1 -> строка 5: #
	// bit6 = 1 -> строка 6: #
	// bit7 = 0 -> не используется
	//
	// значит один столбец 0x7E выглядит так:
	//
	// .
	// #
	// #
	// #
	// #
	// #
	// #
	//
	// а символ:
	// {0x7E,0x11,0x11,0x11,0x7E}
	//
	// означает:
	//
	// столбец 0 = 0x7E
	// столбец 1 = 0x11
	// столбец 2 = 0x11
	// столбец 3 = 0x11
	// столбец 4 = 0x7E

